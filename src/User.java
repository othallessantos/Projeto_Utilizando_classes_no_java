public class User {
    /*
    A simple system to know the name, the age and if he (specifically a man)
    live with his parents or not.
     */

    String name;
    int age;
    boolean liveAlone;

    public User(String name, int age, boolean liveAlone){
        this.name = name;
        this.age = age;
        this.liveAlone = liveAlone;
    }

}